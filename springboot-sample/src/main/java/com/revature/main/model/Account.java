package com.revature.main.model;

public class Account {
	
	private String name;

	public Account() {
		super();
	}

	public Account(String name) {
		super();
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
